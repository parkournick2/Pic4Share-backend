/*
  TABELAS
*/
CREATE TABLE IF NOT EXISTS users (
  nickname VARCHAR(64) UNIQUE PRIMARY KEY,
  email VARCHAR(64) UNIQUE,
  name VARCHAR(64),
  password VARCHAR(64)
);


CREATE TABLE IF NOT EXISTS pictures (
  id VARCHAR(128) UNIQUE PRIMARY KEY,
	subtitle VARCHAR(128),
	author VARCHAR(64),
	date VARCHAR(64),
	file VARCHAR (128),
  tags VARCHAR (128)
);